
/**
 *7.Write a program to print 1 to 10 using the do-while loop statement.
 * @author shruti
 */
public class Loop7 {
    public static void main(String[] args) {
        int i=1;
        do
         {
             System.out.print(i+"\t");
             i++;
         }
        while(i<=10);
    }
}
